    public function update(Request $request, $id)
    {
        //validate the results
        $this->validate($request, [
            'startDetail' => 'required|min:3|max:255',
            'start' => 'required',
            'endDetail' => 'required|min:3|max:255'
        ]);

        $input = $request->all();

        $scale = Scale::findOrFail($id);
        $scale->update($input);

        return redirect('/admin/questions/' . $scale->question . '/edit');

